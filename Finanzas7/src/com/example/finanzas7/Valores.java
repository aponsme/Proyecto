package com.example.finanzas7;

public class Valores {
	private String empresa;
	private String actual;
	private String compra;
	private String beneficio;

	public String getEmpresa() {
		return empresa;
	}

	public void setEmpresa(String title) {
		this.empresa = title;
	}

	public String getActual() {
		return actual;
	}

	public void setActual(String subtitle) {
		this.actual = subtitle;
	}

	public String getCompra() {
		return compra;
	}

	public void setCompra(String subtitle) {
		this.compra = subtitle;
	}


	public String getBeneficio() {
		return beneficio;
	}

	public void setBeneficio(String subtitle) {
		this.beneficio = subtitle;
	}


}